def add(a, b):
    """Adds two integers: a + b

    Args:
        a: first integer
        b: second integer

    Returns:
        Prints the result of a + b
    """
    print(f'{a} + {b} = {a + b}')


def subtract(a, b):
    """Subtracts two integers: a - b

    Args:
        a: first integer
        b: second integer

    Returns:
        Prints the result of a - b
    """
    print(f'{a} - {b} = {a - b}')


def multiply(a, b):
    """multiplies two integers: a + b

    Args:
        a: first integer
        b: second integer

    Returns:
        Prints the result of a * b
    """
    print(f'{a} * {b} = {a * b}')


